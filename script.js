document.addEventListener("keydown", (event) => {
  if (!lightbox.classList.contains("is-open")) return;
  if (event.key === "Escape") closeLightbox();
  if (event.key === "ArrowLeft") moveLightbox(-1);
  if (event.key === "ArrowRight") moveLightbox(1);
});

function setReflexState(nextState) {
  reflexState = nextState;
  reflexStage.classList.remove("waiting", "go", "too-soon");
  if (nextState !== "idle") reflexStage.classList.add(nextState);
}

function formatScore(value) {
  return value ? `${value} ${tr("reflex.result")}` : "--";
}

function updateReflexStats(score) {
  if (score) reflexScores.push(score);
  const best = reflexScores.length ? Math.min(...reflexScores) : 0;
  const avg = reflexScores.length ? Math.round(reflexScores.reduce((sum, item) => sum + item, 0) / reflexScores.length) : 0;
  reflexLast.textContent = formatScore(score || 0);
  reflexBest.textContent = formatScore(best);
  reflexAverage.textContent = formatScore(avg);
}

function startReflexTest() {
  window.clearTimeout(reflexTimer);
  setReflexState("waiting");
  reflexStatus.textContent = tr("reflex.wait");
  reflexResult.textContent = "--";
  const delay = 1400 + Math.round(Math.random() * 2600);
  reflexTimer = window.setTimeout(() => {
    setReflexState("go");
    reflexStatus.textContent = tr("reflex.go");
    reflexGoTime = performance.now();
  }, delay);
}

function stopReflexTest() {
  if (reflexState === "waiting") {
    window.clearTimeout(reflexTimer);
    setReflexState("too-soon");
    reflexStatus.textContent = tr("reflex.tooSoon");
    reflexResult.textContent = "--";
    window.setTimeout(() => {
      setReflexState("idle");
      reflexStatus.textContent = tr("reflex.ready");
    }, 900);
    return;
  }
  if (reflexState !== "go") return;
  const score = Math.max(0, Math.round(performance.now() - reflexGoTime));
  setReflexState("idle");
  reflexStatus.textContent = tr("reflex.ready");
  reflexResult.textContent = `${score} ${tr("reflex.result")}`;
  updateReflexStats(score);
}

reflexStart.addEventListener("click", startReflexTest);
reflexStop.addEventListener("click", stopReflexTest);
reflexStage.addEventListener("click", () => {
  if (reflexState === "idle") startReflexTest();
  else stopReflexTest();
});

